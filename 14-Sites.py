import webbrowser

# Abrir uma aba no navegador existente ou criar outro
webbrowser.open('https://www.adorocinema.com/')

# Abrir uma nova aba eum um navegador existente ou criar outro.
webbrowser.open_new_tab('https://www.adorocinema.com/')

# Criar e abrir uma nova janela do zero
webbrowser.open_new('https://www.adorocinema.com/')